DATATYPES
INT : integer
VARCHAR (n): Variable-length strings, n=max length VARCHAR(255)
DATETIME : Store date and time Values
BOOLEAN : Holds TRUE or FALSE

BASIC OPERATIONS:
SELECT : Retrieves data from tables
WHERE , AND , OR : Filters results based on conditions

Relations between Tables:
- One to One : a row in a table  linked to one row in another TABLE
- One to MAny : a row in a table linked to multiple rows in another
- Many to one : Multiple rows in a table are linked to one row in another
- Many to Many : Multiple rows in a tbale are linked to multiple rows in another

Exemple
SELECT * (* = all / everything) FROM books(in our case books = table)